from typing import Optional

from pydantic import BaseModel


class AgentRun(BaseModel):
    """
    Agent run that records will be associated with.
    """
    run_id: int  # ID of the run (Empower/Fabric ID/Process Run ID)

    # Organization is a concept from Empower/Fabric
    # Example organization link: https://app.thoughtful-dev.ai/admin/organizations/32
    organization_id: int  # Example: 32
    organization_name: str  # Example: American Vision Partners

    id: Optional[str] = None  # Generated by the Reporting database (Do not manually specify)

